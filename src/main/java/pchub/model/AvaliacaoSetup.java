package pchub.model;

public class AvaliacaoSetup {

    private String cpu;
    private String ramGb;
    private String memoriaTipo;
    private String gpu;
    private String armazenamento;
    private String placaMae;
    private String socket;
    private String cooler;
    private String fonteWatts;

    // Getters e Setters
    public String getCpu() {
        return cpu;
    }

    public void setCpu(String cpu) {
        this.cpu = cpu;
    }

    public String getRamGb() {
        return ramGb;
    }

    public void setRamGb(String ramGb) {
        this.ramGb = ramGb;
    }

    public String getMemoriaTipo() {
        return memoriaTipo;
    }

    public void setMemoriaTipo(String memoriaTipo) {
        this.memoriaTipo = memoriaTipo;
    }

    public String getGpu() {
        return gpu;
    }

    public void setGpu(String gpu) {
        this.gpu = gpu;
    }

    public String getArmazenamento() {
        return armazenamento;
    }

    public void setArmazenamento(String armazenamento) {
        this.armazenamento = armazenamento;
    }

    public String getPlacaMae() {
        return placaMae;
    }

    public void setPlacaMae(String placaMae) {
        this.placaMae = placaMae;
    }

    public String getSocket() {
        return socket;
    }

    public void setSocket(String socket) {
        this.socket = socket;
    }

    public String getCooler() {
        return cooler;
    }

    public void setCooler(String cooler) {
        this.cooler = cooler;
    }

    public String getFonteWatts() {
        return fonteWatts;
    }

    public void setFonteWatts(String fonteWatts) {
        this.fonteWatts = fonteWatts;
    }
}
